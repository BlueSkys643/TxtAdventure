public class Character {

    //Establish variables
      public String name;
      public int hp;
      public int mp;
      public int atk;
      public int def;
    public Character(String name1, int HP, int MP, int Attack, int Defense) {
        this.name = name1;
        this.hp = HP;
        this.mp = MP;
        this.atk = Attack;
        this.def = Defense;
    }
}
